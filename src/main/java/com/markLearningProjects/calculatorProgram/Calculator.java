package com.markLearningProjects.calculatorProgram;

public class Calculator {
    //add method
    int addOperation(int x, int y){
        int z = x+y;
        return z;
    }
    // sub method
    int subOperation(int x, int y){
        int z = x-y;
        return z;
    }
    // div method
    int divOperation(int x, int y){
        int z = x/y;
        return z;
    }
    //mul method
    int mulOperation(int x, int y){
        int z = x*y;
        return z;
    }
    //mod method
    int modOperation(int x, int y){
        int z = x%y;
        return z;
    }
}
